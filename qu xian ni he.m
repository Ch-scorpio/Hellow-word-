% 将所有数据组放入一个元胞数组 (Cell Array) 中统一管理
allData = {data1}; 

% ==========================================
% 2. 初始化图形窗口 (都在这同一个图上绘制)
% ==========================================
figure('Name', '多组曲线对比', 'Position', [100, 100, 800, 600]);
hold on; % 关键命令：保持当前图像，允许叠加绘制
grid off;
axis equal;
title('多组二维坐标点平滑拟合对比', 'FontSize', 14);
xlabel('X 轴', 'FontSize', 12); 
ylabel('Y 轴', 'FontSize', 12);

colorOrder = lines(length(allData)); % 根据数据组数获取对应的颜色

% ==========================================
% 3. 循环遍历所有数据并绘制在同一个图上
% ==========================================
for i = 1:length(allData)
    pts = allData{i};
    x = pts(:, 1);
    y = pts(:, 2);
    currentColor = colorOrder(i, :);
    
    % 绘制原始输入点 (圆圈)
    plot(x, y, 'o', 'MarkerEdgeColor', currentColor, 'MarkerFaceColor', 'w', ...
        'MarkerSize', 3, 'HandleVisibility', 'off');

    % 参数化样条插值进行平滑拟合
    if length(x) == 2
        plot(x, y, '-', 'Color', currentColor, 'LineWidth', 2, ...
            'DisplayName', sprintf('第 %d 组曲线', i));
    else
        d = sqrt(diff(x).^2 + diff(y).^2);
        t = [0; cumsum(d)]; 
        tt = linspace(0, t(end), 1000);
        xx = spline(t, x, tt);
        yy = spline(t, y, tt);
        
        plot(xx, yy, '-', 'Color', currentColor, 'LineWidth', 1, ...
            'DisplayName', sprintf('第 %d 组曲线', i));
    end
end

% 添加图例并取消图像保持
